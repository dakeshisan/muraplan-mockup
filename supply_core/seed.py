# приёмка Хранителем (сдвиг дня приёмки от сегодня) — источник «в срок»
ACCEPTED_OFFSET = {"Газоблок D500": -1, "Окна ПВХ": 0}


def build(anchor=None, anchor_now=None):
    A = anchor or today()
    N = anchor_now or now()
    out = []
    for mat, qty, work, wuid, plot, off, lead, stage, ag, kw in _SEED:
        need = (A + timedelta(days=off)).isoformat()
        since = (N - timedelta(days=ag)).isoformat(timespec="seconds")
        out.append(Request(mat, qty, work, wuid, plot, need, lead, stage, since, **kw))
    return out


def accepted_date(mat, anchor=None):
    A = anchor or today()
    return (A + timedelta(days=ACCEPTED_OFFSET[mat])).isoformat()


